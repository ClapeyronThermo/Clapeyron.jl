function dHS_BH(T̄ ,m)
    a = (72564575e-01,1.44851940e-02,-4.47700974e-04)
    b = (2.41366577e-01,9.94732182e-03,-2.83801421e-04,-2.48237845e-06)
    c = (1.44734716e-03,-4.97470110e-05,2.32705654e-07)
    A = evalpoly(a,m)
    B = evalpoly(b,m)
    C = evalpoly(c,m)
    return (one(T̄) + A*T̄)/evalpoly((one(B),B,C),T̄)
end

