function dHS_WCA(T̄,m)
    m == 12 && return 2/(1+sqrt(T̄))^(1/6)
    a = (0.0,7.08059224e-01, 5.52970256e-01, 5.33848228e-01)
    b = (0.0,2.37463060e-01, 1.91956811e-01, -3.99938968e-02)
    c = (0.0,5.75476615e-01, 8.20345330e-01, 5.03543986e-01)
    d = (0.0,4.90928991e-01, 1.49870438e-01, -3.71426331e-02)
    e = (0.0,8.15769030e-03, -3.43788879e-03, 4.81941979e-04)
    minv = 1/m
    A = evalpoly(a,minv)
    B = evalpoly(b,minv)
    C = evalpoly(c,minv)
    D = evalpoly(d,minv)
    E = evalpoly(e,minv)
    y = T̄-1
    num = (one(m),A,B)
    den = (one(m),C,D,E)
    return evalpoly(num,y)/evalpoly(den,y)
end
