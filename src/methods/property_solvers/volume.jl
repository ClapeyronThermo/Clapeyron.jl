function _volume_compress_old(model,p,T,z=SA[1.0],V0=x0_volume(model,p,T,z,phase=:liquid),max_iters=100)
    _0 = zero(Base.promote_eltype(model,p,T,z,V0))
    _1 = one(_0)
    isnan(V0) && return _0/_0
    p‚ÇÄ = _1*p
    XX = typeof(p‚ÇÄ)
    nan = _0/_0
    logV0 = primalval(log(V0)*_1)
    log_lb_v = log(primalval(lb_volume(model,T,z)))
    if iszero(p‚ÇÄ) & (V0 == Inf) #ideal gas
        return _1/_0
    end
    function logstep(logV·µ¢::TT) where TT
        logV·µ¢ < log_lb_v && return TT(zero(logV·µ¢)/zero(logV·µ¢))
        V·µ¢ = exp(logV·µ¢)
        _p·µ¢,_dpdV·µ¢ = p‚àÇp‚àÇV(model,V·µ¢,T,z)
        p·µ¢,dpdV·µ¢ = primalval(_p·µ¢),primalval(_dpdV·µ¢) #ther could be rare cases where the model itself has derivative information.
        dpdV·µ¢ > 0 && return TT(zero(logV·µ¢)/zero(logV·µ¢)) #inline mechanical stability.
        abs(p·µ¢-p‚ÇÄ) < 3eps(p‚ÇÄ) && return TT(zero(V·µ¢)) #this helps convergence near critical points.
        Œî·µ¢ = (p‚ÇÄ-p·µ¢)/(V·µ¢*dpdV·µ¢) #(_p - pset)*Œ∫
        return TT(Œî·µ¢)
    end
    function f_fixpoint(logV·µ¢::TT) where TT
        res = TT(logV·µ¢ + logstep(logV·µ¢))
        return res
    end

    logV = @nan(Solvers.fixpoint(f_fixpoint,logV0,Solvers.SSFixPoint(),rtol = 1e-12,max_iters=max_iters)::XX,nan)
    return exp(logV)
