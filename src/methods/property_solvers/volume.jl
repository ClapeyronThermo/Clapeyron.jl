function _volume_compress(model,p,T,z=SA[1.0],V0=x0_volume(model,p,T,z,phase=:liquid),max_iters=100)
    _0 = zero(Base.promote_eltype(model,p,T,z,V0))
    _1,nan = one(_0),_0/_0
    isnan(V0) && return nan
    p‚ÇÄ = _1*p
    logV0 = log(V0)*_1
    log_lb_v = log(lb_volume(model,T,z))
    logV·µ¢ = logV0
    iszero(p‚ÇÄ) && (V0 == Inf) && return _1/_0 #ideal gas

    nRT = sum(z)*T*Rgas(model)
    
    V·µ¢ = exp(logV·µ¢)
    p·µ¢,dpdV·µ¢ = p‚àÇp‚àÇV(model,V·µ¢,T,z)
    p0·µ¢,dp0dV·µ¢ = p·µ¢,dpdV·µ¢

    check_sp = true
    is_gas = -nRT/V·µ¢/V·µ¢ - dpdV·µ¢ >= _0
    
    _logV·µ¢,_p·µ¢,_dpdV·µ¢ = logV·µ¢,p·µ¢,dpdV·µ¢
    logV_new,logV_bisec = _0,_0
    plo,phi = _0,_0
    
    for i in 1:max_iters

        logV·µ¢ < log_lb_v && return nan

        dpdV·µ¢ > 0 && return nan #inline mechanical stability.
        abs(p·µ¢-p‚ÇÄ) < 3eps(p‚ÇÄ) && return V·µ¢ #this helps convergence near critical points.

        #zeroth order check:
        #the pressure slope between initial point and current point, must be negative.
        #a positive slope means we jumped across an spinodal
        dlogv = (logV·µ¢ - logV0)
        dp = (p·µ¢ - p0·µ¢)
        m = (p·µ¢ - p0·µ¢)/(logV·µ¢ - logV0)

        if m > _0 && i > 1 && sqrt(abs(dlogv/logV·µ¢)) > 5e-8 && sqrt(abs(dp/p·µ¢)) > 5e-8
            #@warn "zeroth order check failed with $(model) at p = $p, T = $T, z= $z, v0 = $V0"
            #@info "pi = $p·µ¢, Vi = $(exp(logV·µ¢))"
            return nan
        end
       
        #=
        ‚àÇp‚àÇV heuristic:
        at the ideal gas, ‚àÇp‚àÇV = -RT/V^2
        as we decrease the volume, ‚àÇp‚àÇV disminishes until reaching zero.

        so ‚àÇp‚àÇV(ideal) > ‚àÇp‚àÇV in the gas phase.

        at the liquid phase, we have initial ‚àÇp‚àÇV << ‚àÇp‚àÇV(ideal) (except near the critical point)

        in any case, along the sequence of fixed points generated by the volume iteration,

        Œî‚àÇp‚àÇV = ‚àÇp‚àÇV(ideal) - ‚àÇp‚àÇV(iteration = i) should have the same sign.
        
        If there is any change in sign, then we can check if there are any spinodals, just to be sure.
        =#
        is_gas_i = -nRT/V·µ¢/V·µ¢ - dpdV·µ¢ >= _0
        if i > 1 && is_gas_i != is_gas
            #@warn "first order check failed with $(model) at p = $p, T = $T, z= $z, v0 = $V0"
            if check_sp
                v_lb,v_ub = minmax(V·µ¢,exp(logV0))
                _maybe_spinodal(model,T,v_lb,v_ub,z) && return nan
                check_sp = false
            end
        end
        
        if i > 5 && (p‚ÇÄ-p·µ¢)*(p‚ÇÄ-_p·µ¢) < 0
            #bracket found, use bracketing updates instead.
            _V·µ¢ = exp(_logV·µ¢)
            data1 = _V·µ¢,_logV·µ¢,_p·µ¢,_dpdV·µ¢
            data2 = V·µ¢,logV·µ¢,p·µ¢,dpdV·µ¢
            return _volume_compress_bisection(model,p‚ÇÄ,T,z,data1,data2,i,max_iters)
        end

        _logV·µ¢,_p·µ¢,_dpdV·µ¢ = logV·µ¢,p·µ¢,dpdV·µ¢
        Œî·µ¢ = (p‚ÇÄ-p·µ¢)/(V·µ¢*dpdV·µ¢) #(_p - pset)*Œ∫
        logV·µ¢ = logV·µ¢ + Œî·µ¢
        V·µ¢ = exp(logV·µ¢)
        p·µ¢,dpdV·µ¢ = p‚àÇp‚àÇV(model,V·µ¢,T,z)

        converged,finite = Solvers.convergence(_logV·µ¢,logV·µ¢,zero(logV·µ¢),1e-12)
        if converged 
            if finite
                return exp(logV·µ¢)
            else
                return nan
            end
        end
        end
    return nan
end

function _volume_compress_bisection(model,p,T,z,data1,data2,i0,max_iters)
    v1,logv1,p1,dpdv1 = data1
    v2,logv2,p2,dpdv2 = data2
    
    if p1 > p2
        vhi,vlo = v1,v2
        logvhi,logvlo = logv1,logv2
        phi,dpdvhi = p1,dpdv1
        plo,dpdvlo = p2,dpdv2
    else
        vlo,vhi = v1,v2
        logvlo,logvhi = logv1,logv2
        phi,dpdvhi = p2,dpdv2
        plo,dpdvlo = p1,dpdv1
    end

    Œîlo = (p-plo)/(vlo*dpdvlo)
    Œîhi = (p-phi)/(vhi*dpdvhi)
    
    _0,_1 = zero(p),oneunit(p)
    for i in i0:max_iters
        #Œ¥ is the needed to remain inside the bracket
        Œ¥lo = clamp((logvhi - logvlo)/Œîlo,_0,_1)
        Œ¥hi = clamp((logvlo - logvhi)/Œîhi,_0,_1)
        iszero(Œîhi) && return vhi
        iszero(Œîlo) && return vlo

        #0.95 damping to stop alternating values
        logv_new_hi = logvhi + 0.95*Œ¥hi*Œîhi
        logv_new_lo = logvlo + 0.95*Œ¥lo*Œîlo

        #mean between the two damped, clamped iterations should be by definition, also inside the pressure bracket.
        logv_new = 0.5*(logv_new_hi + logv_new_lo)
        v_new = exp(logv_new)
        p_new,dpdv_new = p‚àÇp‚àÇV(model,v_new,T,z)
        if (p_new-p)*(plo - p) < _0
            vhi,logvhi,phi,dpdvhi = v_new,logv_new,p_new,dpdv_new
            Œîhi = (p-phi)/(vhi*dpdvhi)
        elseif (p_new - p)*(phi - p) < _0
            vlo,logvlo,plo,dpdvlo = v_new,logv_new,p_new,dpdv_new
            Œîlo = (p-plo)/(vlo*dpdvlo)
        else
            break
        end
        logv_tol = abs(logvlo - logvhi)
        logv_tol < 1e-12 && break

    end
    return sqrt(vlo*vhi)
end

function _maybe_spinodal(model,_T,_v_lb,_v_ub,z)
    T,v_lb,v_ub = promote(_T,_v_lb,_v_ub)
    isnan(v_lb) && return true
    isnan(v_ub) && return true
    p(x) = pressure(model,x,T,z)
    fl,dfl,d2fl = p‚àÇp‚àÇ2p(model,v_lb,T,z)
    fv,dfv,d2fv = p‚àÇp‚àÇ2p(model,v_ub,T,z)
    nan = zero(fl)/zero(fl)
    _0 = zero(nan)
    poly = Solvers.hermite5_poly(v_lb,v_ub,fl,fv,dfl,dfv,d2fl,d2fv)
    dpoly = Solvers.polyder(poly)

    #we already have a bracket.
    dfl*dfv < 0 && return true

    #find the middle point between the liquid and vapour spinodals.
    vm = _find_vm(dpoly,v_lb,v_ub)
    fm,dfm = p‚àÇp‚àÇV(model,vm,T,z)
    dfm > 0 && return true
    #find the liquid of gas spinodal using the quintic hermite interpolation.
    v_bracket_1 = minmax(_0,vm - v_lb)
    v_bracket_2 = minmax(v_ub - v_lb,vm - v_lb)

    no_hermite_bracket_1 = !(evalpoly(_0,dpoly)*evalpoly(vm - v_lb,dpoly) < 0)
    no_hermite_bracket_2 = !(evalpoly(v_ub - v_lb,dpoly)*evalpoly(vm - v_lb,dpoly) < 0)
    
    no_hermite_bracket_1 && no_hermite_bracket_2 && return false

    v1_hermite_prob = Roots.ZeroProblem(Base.Fix2(evalpoly,dpoly),v_bracket_1)
    v2_hermite_prob = Roots.ZeroProblem(Base.Fix2(evalpoly,dpoly),v_bracket_2)
    vh1 = Roots.solve(v1_hermite_prob,xrtol = 1e-5) + v_lb
    vh2 = Roots.solve(v2_hermite_prob,xrtol = 1e-5) + v_lb

    fh1,dfh1 = p‚àÇp‚àÇV(model,vh1,T,z)
    fh2,dfh2 = p‚àÇp‚àÇV(model,vh2,T,z)

    return (dfh1 > 0) | (dfh2 > 0)
end

#"chills" a state from T0,p to T,p, starting at v = v0
function volume_chill(model::EoSModel,p,T,z,v0,T0,Ttol = 0.01,max_iters=100)
    _1 = one(Base.promote_eltype(model,p,T,z))
    v·µ¢ = _1*v0
    T·µ¢ = _1*T0
    count_invalid_iters = 0
    for i in 1:100
        d¬≤A,dA,_ = ‚àÇ2f(model,v·µ¢,T·µ¢,z)
        ‚àÇ¬≤A‚àÇV‚àÇT = d¬≤A[1,2]
        ‚àÇ¬≤A‚àÇV¬≤ = d¬≤A[1,1]
        ‚àÇ¬≤A‚àÇT¬≤ = d¬≤A[2,2]
        p·µ¢ = -dA[1]
        dvdt = -‚àÇ¬≤A‚àÇV‚àÇT/‚àÇ¬≤A‚àÇV¬≤
        dvdp = -1/‚àÇ¬≤A‚àÇV¬≤
        dtdp = -1/‚àÇ¬≤A‚àÇV‚àÇT
        ŒîT = dtdp*(p - p·µ¢)
        Tnew = T·µ¢ + dtdp*(p - p·µ¢)
        if Tnew < T
            T·µ¢ = (T·µ¢ + T)/2
            v·µ¢ = v·µ¢ + dvdp*(p - p·µ¢) + dvdt*(T - T·µ¢)
        else
            T·µ¢ = T·µ¢ + dtdp*(p - p·µ¢)
        end
        Œîv = dvdp*(p - p·µ¢) + dvdt*(T - T·µ¢)
        vnew = v·µ¢ + Œîv
        if vnew > 0
            v·µ¢ = v·µ¢ + dvdp*(p - p·µ¢) + dvdt*(T - T·µ¢)
            count_invalid_iters = 0
        else
            count_invalid_iters +=1
        end
        if count_invalid_iters >= 10
            v·µ¢ = zero(v·µ¢)/zero(v·µ¢)
            break
        end
        abs(ŒîT) < Ttol*T && vnew > 0 && break
        !isfinite(v·µ¢) && break
    end
    return v·µ¢
end

"""
    volume_virial(model::EoSModel,p,T,z=SA[1.0])
    volume_virial(B::Real,p,T,z=SA[1.0])
Calculates an approximation to the gas volume at specified pressure `p`, temperature `T` and composition `z`, by aproximating:
```julia
Z(v) ‚âà 1 + B(T)/v
```
where `Z` is the compressibility factor and `B` is the second virial coefficient.
If `B>0`, (over the inversion temperature) returns `NaN`. If the solution to the problem is complex (`Z = 1 + B/v` implies solving a quadratic polynomial), returns `-2*B`.
If you pass an `EoSModel` as the first argument, `B` will be calculated from the EoS at the input temperature `T`. You can provide your own second virial coefficient instead of a model.
"""
function volume_virial end

function volume_virial(model::EoSModel,p,T,z=SA[1.0])
    B = second_virial_coefficient(model,T,z)
    return volume_virial(B,p,T,z,R = Rgas(model))
end

function volume_virial(B::Real,p,T,z=SA[1.0];R = RÃÑ)
    _0 = zero(B)

    #=
    PV/RT‚àëz = 1 + B/V // a = P/RT‚àëz, .*= V
    aV2 = V + B
    aV2 - V - B = 0
    =#
    B > _0 && return _0/_0
    a = p/(R *T*sum(z))
    b = -1
    c = -B
    Œî = b*b-4*a*c
    if Œî <= 0
        #virial approximation could not be calculated
        #return value at spinodal
        return -2*B
    end
    #only the left root has physical meaning
    return (-b + sqrt(Œî))/(2*a)
end

function pressure_virial(model,V,T,z)
    B = second_virial_coefficient(model,T,z)
    return pressure_virial(B,V,T,z,Rgas(model))
end

function pressure_virial(B::Real,V,T,z,R = RÃÑ)
    Z = 1 + B/V
    return Z*sum(z)*R*T/V
end

